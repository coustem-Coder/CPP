#include<iostream>
#include<cmath>
using namespace std;

int main(){
    cout<<"Sqrt() : "<<sqrt(25)<<endl;
    cout<<"max() : "<<max(3,7)<<endl;
    cout<<"min() : "<<min(3,7)<<endl;
    cout<<"cbrt() : "<<cbrt(8)<<endl;
    cout<<"abs() : "<<abs(-10)<<endl;
    cout<<"floor() : "<<floor(4.65)<<endl;
    cout<<"ceil() : "<<ceil(4.65)<<endl;
    
}